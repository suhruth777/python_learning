print(name.upper()) #Makes everything upper case
print(name.lower()) #Makes everything lower case
print(name.isdigit()) #Will check is string is a digit
print(name.isalpha()) #Will check if string has alphabet
print(name.count("a")) #Will count how many times a character appears
print(name.replace("i", '1')) #Will repalce first quote with second quote
print(name * 3) #Will display string 3 times

