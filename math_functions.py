import math

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi)) #rounds to nearest whole number

print(math.ceil(pi)) #rounds up

print(math.floor(pi)) #rounds down

print(abs(pi)) #returns absolute value

print(pow(pi, 2)) #raises pi to the power of 2

print(math.sqrt(pi)) #takes sqaure root of number

print(max(x,y,z)) #finds largest value

print(min(x,y,z)) #finds smallest value





